const I18N = {
  lang: localStorage.getItem('gl_lang') || 'es',

  t(key, fallback) {
    const val = I18N_DATA[this.lang]?.[key];
    if (val !== undefined) return val;
    // Fallback to English
    return I18N_DATA['en']?.[key] ?? fallback ?? key;
  },

  setLang(lang) {
    this.lang = lang;
    localStorage.setItem('gl_lang', lang);
    // Re-render current screen
    if (typeof GL_APP !== 'undefined') {
      GL_APP.buildTopbar();
      GL_APP.buildSidebar();
      GL_APP.navigateTo(GL_APP.currentScreen);
    }
  },

  toggle() {
    this.setLang(this.lang === 'en' ? 'es' : 'en');
  },

  getLangLabel() {
    return this.lang === 'en' ? '🇪🇸 ES' : '🇬🇧 EN';
  }
};

window.GL_I18N = I18N;
// Shorthand
window.__ = (key, fb) => I18N.t(key, fb);
